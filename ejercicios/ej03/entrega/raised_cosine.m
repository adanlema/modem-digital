function y = raised_cosine (t, Ts, Beta)
%-----------------------------------------------------------

  %---------------------------------------
  y = 1/Ts*sinc(t/Ts).*cos(pi*Beta*t/Ts)./(1-(2*Beta*t/Ts).^2);
  y(t==Ts/2/Beta) = pi/4/Ts*sinc(1/2/Beta);
  y(t==-Ts/2/Beta) = pi/4/Ts*sinc(1/2/Beta);
  %---------------------------------------

end
